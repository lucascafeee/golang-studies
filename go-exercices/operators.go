

// Operadores aritméticos
a := 10
b := 5
c := a + b      // c = 15
d := a - b      // d = 5
e := a * b      // e = 50
f := a / b      // f = 2
g := a % b      // g = 0

// Operadores de atribuição
a := 10
a += 5      // a = a + 5 = 15
a -= 3      // a = a - 3 = 12
a *= 2      // a = a * 2 = 24
a /= 4      // a = a / 4 = 6
a %= 2      // a = a % 2 = 0

// Operadores de Comparação
